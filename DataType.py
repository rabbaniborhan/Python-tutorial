#int: Integer numbers (whole numbers)
age = 25
count = -10

#float: Floating point numbers (decimal numbers)
height = 5.9
temperature = -3.5

#str: Text strings (sequence of characters)
name = "Alice"
message = 'Hello World'

#bool: Boolean values (True or False)
is_student = True
is_raining = False

#list: Ordered, mutable collection (can be changed)
fruits = ["apple", "banana", "cherry"]
numbers = [1, 2, 3, 4, 5]

#Tuple: Ordered, immutable collection (cannot be changed)
coordinates = (10.5, 20.3)
colors = ("red", "green", "blue")

#set: Unordered collection of unique elements
unique_numbers = {1, 2, 3, 4, 5}
vowels = {'a', 'e', 'i', 'o', 'u'}

#dict: Key-value pairs (like a dictionary)
person = {"name": "John", "age": 30, "city": "New York"}

#NoneType: Represents absence of value
result = None


print(type(age))
print(type(height))
print(type(name))
print(type(is_student))
print(type(fruits))
print(type(coordinates))
print(type(unique_numbers))
print(type(person))
print(type(result))